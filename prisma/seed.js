async function main() {
  console.log("üå± Starting deterministic smart city seed...");

  // Reset tables to ensure predictable datasets for dashboard testing
  await prisma.$transaction([
    prisma.serviceRequestStatusLog.deleteMany(),
    prisma.statusLog.deleteMany(),
    prisma.attachment.deleteMany(),
    prisma.complaintPhoto.deleteMany(),
    prisma.material.deleteMany(),
    prisma.notification.deleteMany(),
    prisma.message.deleteMany(),
    prisma.serviceRequest.deleteMany(),
    prisma.complaint.deleteMany(),
    prisma.user.deleteMany(),
    prisma.subZone.deleteMany(),
    prisma.ward.deleteMany(),
    prisma.department.deleteMany(),
    prisma.systemConfig.deleteMany(),
    prisma.report.deleteMany(),
    prisma.oTPSession.deleteMany(),
  ]);

  console.log("‚öôÔ∏è Inserting configuration, departments, wards, and sub-zones...");
  await prisma.systemConfig.createMany({ data: systemConfigs });
  await prisma.department.createMany({ data: departments });
  await prisma.ward.createMany({ data: wards });
  await prisma.subZone.createMany({ data: subZones });

  console.log("üë• Creating users across admin, ward, maintenance, citizen, and guest roles...");
  const passwordHashes = {
    admin: await bcrypt.hash("Admin@123", 10),
    officer: await bcrypt.hash("Officer@123", 10),
    maintenance: await bcrypt.hash("Maintenance@123", 10),
    citizen: await bcrypt.hash("Citizen@123", 10),
  };

  const users = userDefinitions.map(({ passwordCategory, ...user }) => ({
    ...user,
    password: passwordCategory === "guest" ? null : passwordHashes[passwordCategory],
  }));

  await prisma.user.createMany({ data: users });

  console.log("üßæ Inserting six months of complaint history with SLA tracking...");
  await prisma.complaint.createMany({ data: complaints });
  await prisma.statusLog.createMany({ data: statusLogs });

  console.log("üìé Adding complaint attachments, site photos, and materials used...");
  await prisma.attachment.createMany({ data: attachments });
  await prisma.complaintPhoto.createMany({ data: complaintPhotos });
  await prisma.material.createMany({ data: materialEntries });

  console.log("üìù Recording citizen and ward service requests with lifecycle logs...");
  await prisma.serviceRequest.createMany({ data: serviceRequests });
  await prisma.serviceRequestStatusLog.createMany({ data: serviceStatusLogs });

  console.log("‚úÖ Seed data ready for analytics, SLA, and workload dashboards.");
}

